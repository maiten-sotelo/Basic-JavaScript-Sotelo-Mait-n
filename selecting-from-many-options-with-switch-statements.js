function caseInSwitch(val) {
  let answer = "";
  // Only change code below this line
  switch(val){
    case val = 1:
      answer = "alpha";
      break;
    case val = 2:
      answer = "beta";
      break;
    case val = 3:
      answer = "gamma";
      break;
    case val = 4:
      answer = "delta";
      break;
  }


  // Only change code above this line
  return answer;
}

caseInSwitch(1);